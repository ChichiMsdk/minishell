#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*new;
	size_t	slen;
	size_t	finish;

	if (!s || start >= ft_strlen(s))
	{
		new = (char *)malloc(sizeof(char));
		if (!new)
			return (0);
		new[0] = '\0';
		return (new);
	}
	slen = ft_strlen(s);
	finish = slen - start;
	if (finish > len)
		finish = len;
	new = (char *)malloc(sizeof(char) * (finish + 1));
	if (!new)
		return (0);
	ft_strlcpy(new, s + start, finish + 1);
	return (new);
}
